#include "libft.h"

int	ft_isalnum(int c)
{
	int	a;
	int	b;
	int	d;

	a = (c >= '0' && c <= '9');
	b = (c >= 'A' && c <= 'Z');
	d = (c >= 'a' && c <= 'z');
	return (a || b || d);
}
